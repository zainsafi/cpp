# for loop is used to iterate over each item in a sequence(lists,strings,tuples etç)

# for loop iterating through list 
numbers = [1,2,3,4,5,6,7,8]
for num in numbers:
    print(num)

print()

# for loop iterating through string
my_name = 'Zain'
for char in my_name:
    print(char) 

print()


            